# Variables

# A variable, in short words, is an identifier which can store values. Those values could be strings, math operations, booleans and more.

# Variables are case sensitive, which means "var" and "Var" are not the same. 

# There are some rules when creating a variable:
# 1. It can contain, lowercases, uppercases, numbers and underscores
# 2. Hyphens are not allowed
# 3. Cannot begin with a number
# 4. The only symbol allowed is "_"
# 5. Cannot be more than one word: "variable one" -> "variable_one"

# it is possible to reassign variables. This means, it is possible to change the values they previously stored.